#!/usr/bin/env node
import fs from 'node:fs/promises';
import path from 'node:path';

const ROOT = process.cwd();
const PATTERN_DIR = path.join(ROOT, 'packages/web/app/(app)/create/patterns');
const OUTPUT_FILE = path.join(ROOT, 'packages/web/app/(app)/create/engine/generated/svg-pattern-modules.ts');

const FILE_TO_PATTERN = {
  'common.svg': 'running_bond',
  'stack.svg': 'stack_bond',
  'flemish.svg': 'flemish_bond',
  'stretcher.svg': 'stretcher_bond',
  'chevron.svg': 'chevron',
  'herringbone.svg': 'herringbone',
  'hexagonal.svg': 'hexagonal',
};

function tokenizePathData(d) {
  return d.match(/[a-zA-Z]|-?\d*\.?\d+(?:e[-+]?\d+)?/g) ?? [];
}

function parsePathPoints(d) {
  const tokens = tokenizePathData(d);
  let index = 0;
  let command = '';
  let x = 0;
  let y = 0;
  let subpathStartX = 0;
  let subpathStartY = 0;
  const points = [];

  const readNumber = () => {
    if (index >= tokens.length) return null;
    const token = tokens[index];
    if (!token || /^[a-zA-Z]$/.test(token)) return null;
    index += 1;
    return Number(token);
  };

  while (index < tokens.length) {
    const token = tokens[index];
    if (token && /^[a-zA-Z]$/.test(token)) {
      command = token;
      index += 1;
    }
    if (!command) break;

    const lower = command.toLowerCase();
    const relative = command === lower;

    if (lower === 'm') {
      const nx = readNumber();
      const ny = readNumber();
      if (nx === null || ny === null) break;
      x = relative ? x + nx : nx;
      y = relative ? y + ny : ny;
      subpathStartX = x;
      subpathStartY = y;
      points.push({ x, y });
      command = relative ? 'l' : 'L';
      continue;
    }

    if (lower === 'l') {
      const nx = readNumber();
      const ny = readNumber();
      if (nx === null || ny === null) continue;
      x = relative ? x + nx : nx;
      y = relative ? y + ny : ny;
      points.push({ x, y });
      continue;
    }

    if (lower === 'h') {
      const nx = readNumber();
      if (nx === null) continue;
      x = relative ? x + nx : nx;
      points.push({ x, y });
      continue;
    }

    if (lower === 'v') {
      const ny = readNumber();
      if (ny === null) continue;
      y = relative ? y + ny : ny;
      points.push({ x, y });
      continue;
    }

    if (lower === 'z') {
      x = subpathStartX;
      y = subpathStartY;
      continue;
    }

    return null;
  }

  if (points.length < 3) return null;
  return points;
}

function parseSvgViewBox(svg) {
  const match = svg.match(/viewBox="([^"]+)"/i);
  if (!match) return null;
  const parts = match[1].trim().split(/\s+/).map(Number);
  if (parts.length !== 4 || parts.some((value) => Number.isNaN(value))) return null;
  return { minX: parts[0], minY: parts[1], width: parts[2], height: parts[3] };
}

function median(values) {
  if (!values.length) return 1;
  const sorted = [...values].sort((a, b) => a - b);
  const mid = Math.floor(sorted.length / 2);
  return sorted.length % 2 === 0 ? (sorted[mid - 1] + sorted[mid]) / 2 : sorted[mid];
}

async function generate() {
  const modules = {};
  const diagnostics = [];

  for (const [filename, patternType] of Object.entries(FILE_TO_PATTERN)) {
    const fullPath = path.join(PATTERN_DIR, filename);
    const svg = await fs.readFile(fullPath, 'utf8');
    const viewBox = parseSvgViewBox(svg);
    if (!viewBox) continue;

    const pathMatches = [...svg.matchAll(/<path[^>]*\sd="([^"]+)"[^>]*>/gi)];
    const tiles = [];

    for (const pathMatch of pathMatches) {
      const d = pathMatch[1];
      if (!d) continue;
      const points = parsePathPoints(d);
      if (!points) continue;
      const minX = Math.min(...points.map((point) => point.x));
      const maxX = Math.max(...points.map((point) => point.x));
      const minY = Math.min(...points.map((point) => point.y));
      const maxY = Math.max(...points.map((point) => point.y));
      const width = maxX - minX;
      const height = maxY - minY;
      if (width <= 0 || height <= 0) continue;

      tiles.push({
        x: minX - viewBox.minX,
        y: minY - viewBox.minY,
        width,
        height,
        clipPath: points.map((point) => ({ x: point.x - minX, y: point.y - minY })),
      });
    }

    const referenceTileWidth = median(tiles.map((tile) => tile.width));
    const referenceTileHeight = median(tiles.map((tile) => tile.height));

    modules[patternType] = {
      viewBoxWidth: viewBox.width,
      viewBoxHeight: viewBox.height,
      referenceTileWidth,
      referenceTileHeight,
      tiles,
    };

    diagnostics.push(`${patternType}: ${tiles.length} tile path(s) from ${filename}`);
  }

  const outputDir = path.dirname(OUTPUT_FILE);
  await fs.mkdir(outputDir, { recursive: true });

  const content = `/* eslint-disable */
// Auto-generated by packages/web/scripts/generate-svg-pattern-modules.mjs
// Do not edit manually. Re-run the generator after updating SVG pattern files.

export interface SvgPatternModuleTile {
  x: number;
  y: number;
  width: number;
  height: number;
  clipPath: Array<{ x: number; y: number }>;
}

export interface SvgPatternModule {
  viewBoxWidth: number;
  viewBoxHeight: number;
  referenceTileWidth: number;
  referenceTileHeight: number;
  tiles: SvgPatternModuleTile[];
}

export const SVG_PATTERN_MODULES: Record<string, SvgPatternModule> = ${JSON.stringify(modules, null, 2)} as const;
`;

  await fs.writeFile(OUTPUT_FILE, content, 'utf8');

  console.log(`Generated ${path.relative(ROOT, OUTPUT_FILE)}`);
  for (const line of diagnostics) {
    console.log(`- ${line}`);
  }
}

generate().catch((error) => {
  console.error(error);
  process.exit(1);
});
